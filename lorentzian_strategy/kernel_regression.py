# Performance timing decorator
def timed_execution(func):
    """Decorator to measure execution time and ensure <100Œºs target"""
    def wrapper(*args, **kwargs):
        start_time = time.perf_counter()
        result = func(*args, **kwargs)
        execution_time = (time.perf_counter() - start_time) * 1_000_000  # Convert to microseconds
        
        # Log warning if execution exceeds target
        if execution_time > 100:
            print(f"WARNING: {func.__name__} took {execution_time:.2f}Œºs (target: <100Œºs)")
        
        return result, execution_time
    return wrapper


